    private void labelcadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_labelcadastrarActionPerformed
        cliente c = new cliente();
        ClienteDAO dao = new ClienteDAO();
        c.setNome(txtnome.getText());
        c.setCpf(Integer.parseInt(txtcpf.getText()));
        c.setEndereco(txtendereco.getText());
        c.setTelefone(Integer.parseInt(txttelefone.getText()));
        txtnome.setText("");
        txtcpf.setText("");
        txtendereco.setText("");
        txttelefone.setText("");
        dao.create(c);
    }//GEN-LAST:event_labelcadastrarActionPerformed

