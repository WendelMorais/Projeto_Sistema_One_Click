    public void readJtable(){
        
        DefaultTableModel modelo = (DefaultTableModel) jTClientes.getModel();
        ClienteDAO cdao = new ClienteDAO();
        
        for(cliente c: cdao.read()){
            
           modelo.addRow(new Object[]{
               c.getNome(),
               c.getCpf(),
               c.getEndereco(),
               c.getTelefone(),
               c.getId()
           });
        }
    }

