    private void txtremoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtremoverActionPerformed
         if(jTClientes.getSelectedRow() != -1){
             cliente c = new cliente();
            ClienteDAO dao = new ClienteDAO();
            
            c.setId((int)jTClientes.getValueAt(jTClientes.getSelectedRow(),4));
            dao.delete(c);
            
            readJtable();
            
         }
         else{
             JOptionPane.showMessageDialog(null, "Selecione uma informação para excluir!!");
         }
    }//GEN-LAST:event_txtremoverActionPerformed

