    private void jTClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTClientesMouseClicked
        int linha = jTClientes.getSelectedRow();
        txtnome.setText(jTClientes.getValueAt(linha, 0).toString());
        txtcpf.setText(jTClientes.getValueAt(linha, 1).toString());
        txtendereco.setText(jTClientes.getValueAt(linha, 2).toString());
        txttelefone.setText(jTClientes.getValueAt(linha, 3).toString());
       
       
    }//GEN-LAST:event_jTClientesMouseClicked

    private void txtattMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtattMouseClicked
        if(jTClientes.getSelectedRow() != -1){
            
        
        cliente c = new cliente();
        ClienteDAO dao = new ClienteDAO();
        
        c.setNome(txtnome.getText());
        c.setCpf(Integer.parseInt(txtcpf.getText()));
        c.setEndereco(txtendereco.getText());
        c.setTelefone(Integer.parseInt(txttelefone.getText()));
        c.setId((int)jTClientes.getValueAt(jTClientes.getSelectedRow(),4));
        dao.update(c); 
        
        txtnome.setText("");
        txtcpf.setText("");
        txtendereco.setText("");
        txttelefone.setText("");
        }// TODO add your handling code here:
    }//GEN-LAST:event_txtattMouseClicked

